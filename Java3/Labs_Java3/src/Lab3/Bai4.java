        txtTen.setBackground(Color.white);
        txtSDT.setBackground(Color.white);
        txtaDiaChi.setBackground(Color.white);
        txtTrinhDo.setBackground(Color.white);
        txtHeDT.setBackground(Color.white);
        txtGhiDanh.setBackground(Color.white);
        txtSoThich.setBackground(Color.white);
        txtTheThao.setBackground(Color.white);
        
