        txtSDT.setText(null);
        txtaDiaChi.setText(null);
        txtTrinhDo.setText(null);
        txtHeDT.setText(null);
        txtGhiDanh.setText(null);
        txtSoThich.setText(null);
        txtTheThao.setText(null);
        
